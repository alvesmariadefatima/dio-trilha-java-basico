public class ExemplosComparacaoJava{
    public static void main(String[] args) {
        int i1 = 10;
        int i2 = 20;
        float f1 = 4.5f;
        float f2 = 2.5f;
        double d1 = 6.5;
        double d2 = 9.5;
        String s1 = "Mary";
        String s2 = "Mary";
        String s3 = "Fatinha";
        boolean b1 = true;
        boolean b2 = false;

        //exemplos de comparação usando operadores relacionais 
        System.out.println(f1 == f2);
        System.out.println(i1 != i2);
        System.out.println(s1 + s2 + s3);
        System.out.println(b1 && b2);
    }
}